function renderMarkdown(content: string) {
  return content
    .split('\n')
    .map((line, i) => {
      if (line.startsWith('```')) return null;
      if (line.startsWith('| ') || line.startsWith('|---')) {
        return (
          <span key={i} className="font-mono text-sm" style={{ fontSize: 12, display: 'block' }}>
            {line}
          </span>
        );
      }
      if (line.startsWith('#')) {
        return (
          <p key={i} style={{ fontWeight: 500, marginTop: 8 }}>
            {line.replace(/^#+\s/, '')}
          </p>
        );
      }
      return (
        <span key={i} style={{ display: 'block' }}>
          {line}
        </span>
      );
    })
    .filter(Boolean);
}

